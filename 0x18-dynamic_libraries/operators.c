/**
 * add - adds to integer values
 * @a: first number
 * @b: second number
 *
 * Return: result of the operation
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts to integer values
 * @a: first number
 * @b: second number
 *
 * Return: result of the operation
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies to integer values
 * @a: first number
 * @b: second number
 *
 * Return: result of the operation
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides to integer values
 * @a: first number
 * @b: second number
 *
 * Return: result of the operation
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mods to integer values
 * @a: first number
 * @b: second number
 *
 * Return: result of the operation
 */
int mod(int a, int b)
{
	return (a % b);
}
